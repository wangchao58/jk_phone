var app = getApp();
// 引用公共js
var fileUpload = require("../../../js/fileUpload.js");
var QQMapWX = require('../../../pages/images/qqmap-wx-jssdk.min.js');
var qqmapsdk; 
const innerAudioContext = wx.createInnerAudioContext();
Page({
  data: {
    photos: [],
    curImgList:[],
    evalList: [{ tempFilePaths: [], imgList: [] }],
    city: '',
    max: 200 //最多字数
  },

  onLoad: function (options) {
    qqmapsdk = new QQMapWX({
      key: app.globalData.mapKey
    });
    this.thisCity();
  },

  onShow:function(){
    innerAudioContext.src = "http://i.bjjkkj.com/sound/sound.mp3";
    innerAudioContext.obeyMuteSwitch = false
  },

  // 获取索要发布的资讯信息
  inputs: function (e) {
    // 获取输入框的内容
    var value = e.detail.value;
    // 获取输入框内容的长度
    var len = parseInt(value.length);

    //最多字数限制
    if (len > this.data.max) {
      return;
    }
    // 当输入框内容的长度大于最大长度限制（max)时，终止setData()的执行
    this.setData({
      currentWordNumber: len, //当前字数 
      informationInput: e.detail.value 
    })
  },

  /**
   * 选择图片方法
   */
  chooseImage: function () {
    var that = this;
    var num = that.data.photos.length;//已上传图片数
    var count = 3 - num;//剩余可上传照片数
    if (num < 3){
      wx.showActionSheet({
        itemList: ["从相册中选择", "拍照"],
        itemColor: "#f7982a",
        success: function (res) {
          if (!res.cancel) {
            if (res.tapIndex == 0) {
              // 调用公共收藏js方法(从相册中选择)
              fileUpload.chooseImg("album", count, function (result) {
                that.setData({
                  photos: result
                });
                that.upLoadImg(result);
              })
            } else if (res.tapIndex == 1) {
              //调用公共收藏js方法(拍照)
              fileUpload.chooseImg("camera", count, function (result) {
                that.setData({
                  photos: result
                });
                that.upLoadImg(result);
              })
            }
          }
        }
      })
    }else{
      wx.showToast({
        title: '最多上传3张图片',
        duration: 2000
      })
    }
  },

  upLoadImg: function (list) {
    var that = this;
    this.upload(that, list);
  },

  /**
   * 上传多张图片
   */
  upload: function (page, path) {
    var that = this;
    console.log(path);
    //公共js
    fileUpload.imageUpload(path, function (result) {
      that.setData({
        photos: result
      })
    })
  },

  //删除已选图片方法
  removeimg: function (e) {
    var photos = this.data.photos;
    var index = e.currentTarget.id
    photos.splice(index, 1)
    this.setData({
      photos: photos
    })
    // this.upLoadImg(photos);
  },

  /**
   * 是否显示城市
   */
  checkboxChange: function () {
    var anonymous = this.data.anonymous;
    if (anonymous) {
      this.setData({ anonymous: false })
      this.thisCity();
    } else {
      this.setData({ 
        anonymous: true,
        city: '' 
      }) 
    }
  },

  /**
   * 获取当前城市名称
   */
  thisCity: function () {
    var that = this;
    wx.getLocation({
      type: 'wgs84',
      success(res) {
        qqmapsdk.reverseGeocoder({
          location: {
            latitude: res.latitude,
            longitude: res.longitude
          },
          success: function (addressRes) {
            console.log(addressRes.result.address_component);
            // const latitude = res.latitude
            // const longitude = res.longitude
            // const speed = res.speed
            // const accuracy = res.accuracy
            // console.log(latitude);
            // console.log(longitude);
            // console.log(speed);
            // console.log(accuracy);
            var address = addressRes.result.formatted_addresses.recommend;
            that.setData({
             city: address
            })
          }
        })
      },
      fail: function() {
        wx.showToast({
          title: '授权失败',
          icon: 'success',
          duration: 1000
        })
      }
    
    })
  },

  /**
   * 发布资讯提交
   */
  informationSub: function () {
    var that = this;
    var informationInput = this.data.informationInput;
    console.log("informationInput" + informationInput)
    if (null != informationInput && app.globalData.userInfo){
      var city = that.data.city;
      var photosUrl = that.data.photos;
      var userId = wx.getStorageSync('userid');
      var longitude = wx.getStorageSync('longitude');
      var latitude = wx.getStorageSync('latitude');
      var src = app.globalData.src + "/information/addInformation";
      wx.request({
        url: src,
        method: 'POST',
        header: { 'content-type': 'application/x-www-form-urlencoded' },
        data: {
          tContent: informationInput,
          pId: userId,
          tCoordinate: longitude + "," + latitude,//坐标，经纬度
          city: city,
          tImg: photosUrl
        },
        success(res) {
          if (res.data > 0) {
            // 声音播放
            innerAudioContext.play();
            wx.showToast({
              title: "发布成功"
            })
            wx.redirectTo({
              url: '../../index/information/information'
            })
           
          } else {
            wx.showToast({
              title: "发布失败"
            })
          }
        }
      })
    }else{
      wx.showModal({
        title: '友情提示',
        content: '内容不能为空或未登录',
       
      })
    }
  }
})